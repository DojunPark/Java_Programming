
public class Main {

	public static void main(String[] args) {
		
		//출력하기
		System.out.println("박도준");
		
		//코드 실행
		System.out.println(10 + 10);
		System.out.println(30 * 30);
		System.out.println(20 - 5);
		System.out.println(40 / 3);
		System.out.println(395 / 18);
		
		//사각형모양의 별 출력
		for (int i = 0; i < 10; i++) {
			for (int j = 0; j < 10; j++ ) {
				System.out.print("*");	
			}
			System.out.println("");
		}
		
	}

}
